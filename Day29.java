
public class Day29 {

    public static void main(String[] args) {
        // Perulangan for
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }

        // Perulangan while
        int i = 1;
        while (i <= 10) {
            System.out.println(i);
            i++;
        }

        // Perulangan do-while
        i = 1;
        do {
            System.out.println(i);
            i++;
        } while (i <= 10);
    }

}
